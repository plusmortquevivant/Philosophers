int	ft_isdigit(int c)
{
	return ('0' <= c && c <= '9');
}

int	parse_arg(const char *nptr)
{
	int	n;

	n = 0;
	while (*nptr)
	{
		if (!ft_isdigit(*nptr))
		{
			return (0);
		}
		n = n * 10 + (*nptr++ - '0');
	}
	return (n);
}
